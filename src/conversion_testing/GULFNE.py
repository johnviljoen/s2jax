    #%%%%%%%%%%%%%%% NONLINEAR ELEMENTS %%%%%%%%%%%%%%%

    @staticmethod
    def eGLF(self, nargout,*args):

        import jax.numpy as jnp
        EV_  = args[0]
        iel_ = args[1]
        V1SQ = EV_[0]*EV_[0]
        YMV2 = 25.0+(-50.0*jnp.log(self.elpar[iel_][0]))**(2.0/3.0)-EV_[1]
        YMV2SQ = YMV2*YMV2
        LNYMV2 = jnp.log(jnp.absolute(YMV2))
        A = jnp.absolute(YMV2)**EV_[2]/EV_[0]
        AM1 = A-1.0
        ALN = A*LNYMV2
        EXPMA = jnp.exp(-A)
        AEXPMA = A*EXPMA
        f_   = EXPMA
        if not isinstance( f_, float ):
            f_   = f_.item();
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = jnp.zeros(dim)
            g_ = jtu.np_like_set(g_, 0, AEXPMA/EV_[0])
            g_ = jtu.np_like_set(g_, 1, EV_[2]*AEXPMA/YMV2)
            g_ = jtu.np_like_set(g_, 2, -AEXPMA*LNYMV2)
            if nargout>2:
                H_ = jnp.zeros((3,3))
                H_ = jtu.np_like_set(H_, jnp.array([0,0]), (A-2.0)*AEXPMA/V1SQ)
                H_ = jtu.np_like_set(H_, jnp.array([0,1]), EV_[2]*AM1*AEXPMA/(EV_[0]*YMV2))
                H_ = jtu.np_like_set(H_, jnp.array([1,0]), H_[0,1])
                H_ = jtu.np_like_set(H_, jnp.array([0,2]), -ALN*AEXPMA/EV_[0])
                H_ = jtu.np_like_set(H_, jnp.array([2,0]), H_[0,2])
                H_ = jtu.np_like_set(H_, jnp.array([1,1]), EV_[2]*AEXPMA*(1.0+EV_[2]*AM1)/YMV2SQ)
                H_ = jtu.np_like_set(H_, jnp.array([1,2]), AEXPMA*(1.0+EV_[2]*ALN)/YMV2)
                H_ = jtu.np_like_set(H_, jnp.array([2,1]), H_[1,2])
                H_ = jtu.np_like_set(H_, jnp.array([2,2]), ALN*LNYMV2*EXPMA*AM1)
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

