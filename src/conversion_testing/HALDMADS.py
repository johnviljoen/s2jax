    #%%%%%%%%%%%%%%% NONLINEAR ELEMENTS %%%%%%%%%%%%%%%

    @staticmethod
    def eHM(self, nargout,*args):

        import jax.numpy as jnp
        EV_  = args[0]
        iel_ = args[1]
        YY = self.elpar[iel_][0]*self.elpar[iel_][0]
        YYY = YY*self.elpar[iel_][0]
        N = EV_[0]+self.elpar[iel_][0]*EV_[1]
        D = 1.0+EV_[2]*self.elpar[iel_][0]+EV_[3]*YY+EV_[4]*YYY
        DD = D*D
        DDD = DD*D
        f_   = N/D
        if not isinstance( f_, float ):
            f_   = f_.item()
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = jnp.zeros(dim)
            g_ = jtu.np_like_set(g_, 0, 1.0/D)
            g_ = jtu.np_like_set(g_, 1, self.elpar[iel_][0]/D)
            g_ = jtu.np_like_set(g_, 2, -N*self.elpar[iel_][0]/DD)
            g_ = jtu.np_like_set(g_, 3, -N*YY/DD)
            g_ = jtu.np_like_set(g_, 4, -N*YYY/DD)
            if nargout>2:
                H_ = jnp.zeros((5,5))
                H_ = jtu.np_like_set(H_, jnp.array([0,2]), -self.elpar[iel_][0]/DD)
                H_ = jtu.np_like_set(H_, jnp.array([2,0]), H_[0,2])
                H_ = jtu.np_like_set(H_, jnp.array([0,3]), -YY/DD)
                H_ = jtu.np_like_set(H_, jnp.array([3,0]), H_[0,3])
                H_ = jtu.np_like_set(H_, jnp.array([0,4]), -YYY/DD)
                H_ = jtu.np_like_set(H_, jnp.array([4,0]), H_[0,4])
                H_ = jtu.np_like_set(H_, jnp.array([1,2]), -YY/DD)
                H_ = jtu.np_like_set(H_, jnp.array([2,1]), H_[1,2])
                H_ = jtu.np_like_set(H_, jnp.array([1,3]), -YYY/DD)
                H_ = jtu.np_like_set(H_, jnp.array([3,1]), H_[1,3])
                H_ = jtu.np_like_set(H_, jnp.array([1,4]), -YY*YY/DD)
                H_ = jtu.np_like_set(H_, jnp.array([4,1]), H_[1,4])
                H_ = jtu.np_like_set(H_, jnp.array([2,2]), 2.0*N*YY/DDD)
                H_ = jtu.np_like_set(H_, jnp.array([2,3]), 2.0*N*YYY/DDD)
                H_ = jtu.np_like_set(H_, jnp.array([3,2]), H_[2,3])
                H_ = jtu.np_like_set(H_, jnp.array([2,4]), 2.0*N*YY*YY/DDD)
                H_ = jtu.np_like_set(H_, jnp.array([4,2]), H_[2,4])
                H_ = jtu.np_like_set(H_, jnp.array([3,3]), 2.0*N*YY*YY/DDD)
                H_ = jtu.np_like_set(H_, jnp.array([3,4]), 2.0*N*YY*YYY/DDD)
                H_ = jtu.np_like_set(H_, jnp.array([4,3]), H_[3,4])
                H_ = jtu.np_like_set(H_, jnp.array([4,4]), 2.0*N*YYY*YYY/DDD)
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

