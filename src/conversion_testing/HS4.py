    @staticmethod
    def gCUBE(self,nargout,*args):

        GVAR_ = args[0]
        igr_  = args[1]
        f_= GVAR_*GVAR_*GVAR_
        if nargout>1:
            g_ = 3.0*GVAR_*GVAR_
            if nargout>2:
                H_ = jnp.zeros((1,1))
                H_ = 6.0*GVAR_
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

