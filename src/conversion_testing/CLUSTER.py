    @staticmethod
    def eTA(self, nargout,*args):

        import jax.numpy as jnp
        EV_  = args[0]
        iel_ = args[1]
        SY = jnp.sin(EV_[1])
        CY = jnp.cos(EV_[1])
        F1 = EV_[0]-EV_[1]*EV_[1]
        F2 = EV_[0]-SY
        DF1DY = -2.0*EV_[1]
        DF2DY = -CY
        f_   = F1*F2
        if not isinstance( f_, float ):
            f_   = f_.item()
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = jnp.zeros(dim)
            g_ = jtu.np_like_set(g_, 0, F2+F1)
            g_ = jtu.np_like_set(g_, 1, DF1DY*F2+F1*DF2DY)
            if nargout>2:
                H_ = jnp.zeros((2,2))
                H_ = jtu.np_like_set(H_, jnp.array([0,0]), 2.0)
                H_ = jtu.np_like_set(H_, jnp.array([0,1]), DF1DY+DF2DY)
                H_ = jtu.np_like_set(H_, jnp.array([1,0]), H_[0,1])
                H_ = jtu.np_like_set(H_, jnp.array([1,1]), 2.0*(DF1DY*DF2DY-F2)+F1*SY)
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

    @staticmethod
    def eTB(self, nargout,*args):

        import jax.numpy as jnp
        EV_  = args[0]
        iel_ = args[1]
        SY = jnp.sin(EV_[1])
        CY = jnp.cos(EV_[1])
        CX = jnp.cos(EV_[0])
        SX = jnp.sin(EV_[0])
        F1 = CY-EV_[0]
        F2 = EV_[1]-CX
        DF1DY = -SY
        DF2DX = SX
        f_   = F1*F2
        if not isinstance( f_, float ):
            f_   = f_.item()
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = jnp.zeros(dim)
            g_ = jtu.np_like_set(g_, 0, F1*DF2DX-F2)
            g_ = jtu.np_like_set(g_, 1, F1+F2*DF1DY)
            if nargout>2:
                H_ = jnp.zeros((2,2))
                H_ = jtu.np_like_set(H_, jnp.array([0,0]), -DF2DX+F1*CX-DF2DX)
                H_ = jtu.np_like_set(H_, jnp.array([0,1]), -SY*DF2DX-1.0)
                H_ = jtu.np_like_set(H_, jnp.array([1,0]), H_[0,1])
                H_ = jtu.np_like_set(H_, jnp.array([1,1]), 2.0*DF1DY-F2*CY)
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

